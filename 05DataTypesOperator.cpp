#include <iostream>

using namespace std;

int main() {
	int num1, num2;
	
	cout << "Enter first number:";
	cin >> num1;
	cout << "Enter second Number:";
	cin >> num2;
	
	cout << "Sum" << num1 + num2 << endl;
	cout << "Difference" << num1 - num2 << endl;
	cout << "Qoutient" << num1 * num2 << endl;
	cout << "Product" << num1 / num2 << endl;
	cout << "Remainder" << num1 % num2 << endl;
	
	return 0;
}

	
	
	
	